import { create } from "zustand";
import { axiosInstance } from "../lib/axios.js";
import toast from "react-hot-toast";
import { useAuthStore } from "./useAuthStore.js";

export const useChatStore = create((set, get) => ({
    allContacts: [],
    chats: [],
    messages: [],
    activeTab: "chats",
    selectedUser: null,
    isUsersLoading: false,
    isMessagesLoading: false,
    isSoundEnabled: JSON.parse(localStorage.getItem("isSoundEnabled")) === true,

    toggleSound() {
        localStorage.setItem("isSoundEnabled", !get.isSoundEnabled);
        set({ isSoundEnabled: !get().isSoundEnabled });
    },
    setActiveTab(tab) {
        set({ activeTab: tab });
    },
    setSelectedUser(user) {
        set({ selectedUser: user });
    },

    async getAllContacts() {
        set({ isUsersLoading: true });
        try {
            const res = await axiosInstance.get("/api/messages/contacts");
            set({ allContacts: res.data.data });
        } catch (error) {
            toast.error(error.response.data.message);
        } finally {
            set({ isUsersLoading: false });
        }
    },

    async getMyChatPartners() {
        set({ isUsersLoading: true });
        try {
            const res = await axiosInstance.get("/api/messages/chats");
            set({ chats: res.data.data });
        } catch (error) {
            toast.error(error.response.data.message);
        } finally {
            set({ isUsersLoading: false });
        }
    },

    async getMessagesByUserId(userId) {
        set({ isMessagesLoading: true });
        try {
            const res = await axiosInstance(`/api/messages/${userId}`);
            set({ messages: res.data.data });
        } catch (error) {
            toast.error(
                error.response?.data?.message || "Something went Wrong",
            );
        } finally {
            set({ isMessagesLoading: false });
        }
    },

    sendMessage: async (messageData) => {
        const { selectedUser, messages } = get();
        const { authUser } = useAuthStore.getState();

        const tempId = `temp-${Date.now()}`;

        const optimisticMessage = {
            _id: tempId,
            senderId: authUser._id,
            receiverId: selectedUser._id,
            text: messageData.text,
            image: messageData.image,
            createdAt: new Date().toISOString(),
            isOptimistic: true, // flag to identify optimistic messages (optional)
        };
        // immidetaly update the ui by adding the message
        set({ messages: [...messages, optimisticMessage] });

        try {
            const res = await axiosInstance.post(
                `/api/messages/send/${selectedUser._id}`,
                messageData,
            );
            set({ messages: messages.concat(res.data.data) });
        } catch (error) {
            // remove optimistic message on failure
            set({ messages: messages });
            toast.error(
                error.response?.data?.message || "Something went wrong",
            );
            console.log(error.response);
        }
    },

    subscribeToMessages(){
        const { selectedUser, isSoundEnabled } = get();
        if (!selectedUser) return;
        const socket = useAuthStore.getState().socket;
        socket.on("newMessage", (newMessage) => {
            const isMessageSentFromSelectedUser =
                newMessage.senderId.toString() === selectedUser._id.toString();
            if (!isMessageSentFromSelectedUser) return;
            const currentMessages = get().messages;
            set({ messages: [...currentMessages, newMessage] });
            if (isSoundEnabled) {
                const notificationSound = new Audio("/sounds/notification.mp3");
                notificationSound.currentTime = 0;
                notificationSound.play().catch((e) => console.log("Audio Play Failed:", e));
            }
        })
    },

    unsubscribeFromMessages() {
        const socket = useAuthStore.getState().socket;
        socket.off("newMessage");
    }
}));
